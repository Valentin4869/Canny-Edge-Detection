% handles    structure with handles and user data (see GUIDATA)



% --- Executes on button press in pb_update.
function pb_update_Callback(hObject, eventdata, handles)
% hObject    handle to pb_update (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

rect=getrect(handles.axes1);


saveim=handles.img;
rect=int32(rect);
[m,n,d]=size(saveim);


ind=rect<=0;
rect(ind)=1;



rect(3)=rect(3)+rect(1);
rect(4)=rect(4)+rect(2);


if rect(3)>n
    rect(3)=n;
end

if rect(4)>m
    rect(4)=m;
end


reg=saveim(rect(2):rect(4),rect(1):rect(3),:);


if handles.checkbox
res=canny2(reg,str2double(handles.edit2.String));
else
res=canny2(reg,str2double(handles.edit2.String),str2double(handles.edit4.String),str2double(handles.edit3.String));    
end




if d<3
handles.img(rect(2):(rect(4)),rect(1):(rect(3)),:)=255*res;    
else
size(handles.img(rect(2):(rect(4)),rect(1):(rect(3)),:));

handles.img(rect(2):(rect(4)),rect(1):(rect(3)),:)=255*cat(3,res,res,res);

end
imshow(handles.img);


